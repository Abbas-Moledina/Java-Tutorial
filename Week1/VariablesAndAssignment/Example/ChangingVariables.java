public class ChangingVariables{
	public static void main(String[] args){
		//The first value is stored in the variable WholeNumber
		int WholeNumber = 123;
		System.out.println("The variable type is integer.Its value is " + WholeNumber);

		//Now change the value stored in the variable
		WholeNumber = 42;
		System.out.println("The variable's type is integer.Its value is " + WholeNumber);
	}
}
