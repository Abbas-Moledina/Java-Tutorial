public class DataTypes{
	public static void main(String[] args){
		//This is a string
		String text = "includes text";
		//This is a integer
		int WholeNumber = 123;
		//This is a decimal 
		double DecimalNumber = 3.14152986;
		//This is a boolean Value.
		boolean istrue = true;

		//To display the value of a variable you use
		//the +
		System.out.println("The variable type is text.Its value is " + text);
		System.out.println("The variable type is a integer.Its value is " + WholeNumber);
		System.out.println("The variable type is a decimal.Its value is " + DecimalNumber);
		System.out.println("The variable type is a boolean.Its value is " + istrue);

	}
}
