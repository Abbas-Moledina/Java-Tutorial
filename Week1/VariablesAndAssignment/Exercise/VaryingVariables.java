public class VaryingVariables{
	public static void main(String[] args){
		//Store the values in variables
		int NumOfChickens = 9000;
		double WeightOfBacon = 0.1;
		String NameOfTractor = "Zetor";

		//Display the items
		System.out.println("Chickens:");
		System.out.println(NumOfChickens);
	
		System.out.println("Bacon (kg):");
		System.out.println(WeightOfBacon);

		System.out.println("A tractor:");
		System.out.println(NameOfTractor);


		//Now just the values
		System.out.println("In a nutshell:");
		System.out.println(NumOfChickens);
		System.out.println(WeightOfBacon);
		System.out.println(NameOfTractor);
	}
}
